# 逻辑运算符
# and 与
"""
True and True  ===> True
True and False ==> False
两边都为真才为真
"""
print(1 > 2)
print(1 and 2)
print(1 > 2 and 10 > 2)
print(5 > 2 and 10 > 3)
# or 或
"""
只要有一个为True 就为True
True or True ===> True
True or False ===> True
False or False ===> False
"""

print(1 > 2 or 1 > 3)
print(5 > 2 or 1 > 3)
print(1 > 2 or 5 > 3)

# not 非，取反
res = not True
print(res)
print(not(1 > 2))
